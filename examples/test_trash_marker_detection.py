async def test_trash_marker_detection():
    """
    测试废片标记识别功能
    
    这个示例展示了如何使用废片标记识别功能。
    """
    print("=" * 80)
    print("废片标记识别功能测试")
    print("=" * 80)
    
    # 测试数据
    asr_infos = TEST_ASR_DATA["asr_infos"]
    
    # 模拟的输入参数
    inputs = {
        "asr": {"asr_infos": asr_infos},
        "user_request": "自动删除包含废片标记的片段",
        "gap_threshold": 400,
        "enable_trash_detection": True,
        "trash_markers": ["这条不行", "重来", "不对", "算了", "不要这个"]
    }
    
    print("\n输入参数:")
    print(json.dumps(inputs, indent=2, ensure_ascii=False))
    
    print("\n原始ASR句子:")
    for asr_info in asr_infos:
        for sentence in asr_info["asr_sentence_info"]:
            print(f"  [{sentence['start']:6d}-{sentence['end']:6d}ms] {sentence['text']}")
    
    print("\n废片标记词:", inputs["trash_markers"])
    print("\n预期结果:")
    print("  - 第一句: 保留 (正常内容)")
    print("  - 第二句: 删除 (包含'这条不行'和'重来')")
    print("  - 第三句: 保留 (正常内容)")
    
    print("\n" + "=" * 80)
    print("测试完成")
    print("=" * 80)
    
